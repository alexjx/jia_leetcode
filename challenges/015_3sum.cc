/**
 * The idea is that, notice the problem only ask the none-duplicated list of
 * values. so we are fine to sort the input array. After that, we could start
 * search from both ends. We start by working from one end of the array. by
 * choosing one integer, then search towards the other end starting from next
 * integer. The problem then equivalent to finding the sum of two numbers equal
 * to the negative value of the chosen one. Instead of using binary search, we
 * working by moving one end at a time. Since the array has been sorted, we
 * could using the principle of binary search. if the sum is greater than
 * target, we lower the high end. Otherwise move the lower end.
 * And one optimization is to skip the duplicated values along the way.
 */
class Solution {
public:
    vector<vector<int>> threeSum(vector<int>& nums)
    {
        vector<vector<int>> result;

        // first sort the input array
        sort(nums.begin(), nums.end());

        // need at least three nums
        for (size_t i = 0; i + 2 < nums.size(); i++) {
            // skip the numbers which are duplicated
            if (i == 0 || nums[i] != nums[i - 1]) {
                // make the problem to be searching for sum of -nums[i]
                auto target = -nums[i];

                // starting from both ends, note we will skip the ith one.
                auto low = i + 1;
                auto high = nums.size() - 1;

                while (low < high) {
                    // working from both ends,
                    // calc temporary sum of the ends.
                    // since the array has been sorted, if the target value is
                    // larger, move the high end otherwise move the low end.
                    auto sum = nums[low] + nums[high];
                    if (target == sum) {
                        // if we have found a target, save it
                        result.push_back({ nums[i], nums[low], nums[high] });

                        // deal with the duplicates, since we have sorted.
                        // we will skip the same values for both ends.
                        while (nums[low] == nums[low + 1] && high > low)
                            low++;

                        while (nums[high - 1] == nums[high] && high > low)
                            high--;

                        // now we move the ends to the next pair
                        // and start again for this target
                        // since there could be multiple sums in the array.
                        low++;
                        high--;

                    } else if (sum < target) {
                        low++;
                    } else if (sum > target) {
                        high--;
                    }
                }
            }
        }

        return result;
    }
};

